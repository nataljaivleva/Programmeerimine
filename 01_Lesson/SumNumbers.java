public class SumNumbers {
    public static void main(String[] args) {
        System.out.println("======== Сложение чисел ===========");
        int number1 = 10;
        int number2 = 15;
        int sum = number1 + number2;
        System.out.println("Сумма двух " + number1 + " и " + number2 + " чисел равна: " + sum);
    }
}